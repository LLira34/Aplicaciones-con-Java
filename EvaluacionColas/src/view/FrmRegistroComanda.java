    private void btnNuevaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNuevaActionPerformed
        // TODO add your handling code here:
        comanda = new Comanda();
        comanda.generarFolio();
        txtFolio.setText(""+comanda.getFolio());
        txtMesa.setText("0");
        txtComida.setText("");
        txtBebida.setText("");
        txtPostre.setText("");
    }//GEN-LAST:event_btnNuevaActionPerformed

    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        // TODO add your handling code here:
        comanda.setComida(txtComida.getText());
        comanda.setBebida(txtBebida.getText());
        comanda.setPostre(txtPostre.getText());
        comanda.setMesa(Integer.parseInt(txtMesa.getText()));
        cola.agregar(comanda);
        txtTotal.setText("" + cola.getOrdenes());
        JOptionPane.showMessageDialog(null, "Comanda agregada");
    }//GEN-LAST:event_btnRegistrarActionPerformed

