    private void btnAtenderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtenderActionPerformed
        // TODO add your handling code here:
        comanda= cola.atender();
        if (comanda!=null) {
            txtComanda.append("Folio: " + comanda.getFolio() + "\n");
            txtComanda.append("Mesa: " + comanda.getMesa() + "\n");
            txtComanda.append("Comidas: " + comanda.getComida() + "\n");
            txtComanda.append("Bebidas: " + comanda.getBebida() + "\n");
            txtComanda.append("Postres: " + comanda.getPostre() + "\n");
            txtTotal.setText(""+ cola.getOrdenes());
        }else{
            JOptionPane.showMessageDialog(null, "Hey!");
        }
    }//GEN-LAST:event_btnAtenderActionPerformed

