    private void btnCalculateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalculateActionPerformed
        // TODO add your handling code here:
        NeuralNetwork nn = new MultiLayerPerceptron(5,10,1); // 2 entradas, 4 en la capa oculta, 1 salida.
        DataSet ds = new DataSet(5,1); // Conjunto de datos de entrenamiento
        
        // Se agregan los pares de entrenamiento
        ds.addRow(new DataSetRow(new double[]{-1,-1,0,0,1}, new double[]{0}));
        ds.addRow(new DataSetRow(new double[]{-1,-1,1,0,-1}, new double[]{1}));
        ds.addRow(new DataSetRow(new double[]{0,-1,0,0,1}, new double[]{0}));
        ds.addRow(new DataSetRow(new double[]{-1,-1,1,-1,1}, new double[]{1}));
        ds.addRow(new DataSetRow(new double[]{1,-1,0,-1,1}, new double[]{0}));
        ds.addRow(new DataSetRow(new double[]{1,-1,0,0,1}, new double[]{0}));
        ds.addRow(new DataSetRow(new double[]{-1,0,0,0,1}, new double[]{0}));
        ds.addRow(new DataSetRow(new double[]{1,-1,0,-1,-1}, new double[]{0}));
        ds.addRow(new DataSetRow(new double[]{0,1,1,1,-1}, new double[]{1}));
        ds.addRow(new DataSetRow(new double[]{0,0,0,0,1}, new double[]{0}));
        ds.addRow(new DataSetRow(new double[]{0,1,0,-1,0}, new double[]{1}));
        ds.addRow(new DataSetRow(new double[]{-1,0,1,1,-1}, new double[]{1}));
        ds.addRow(new DataSetRow(new double[]{0,-1,0,1,1}, new double[]{0}));
        ds.addRow(new DataSetRow(new double[]{0,0,0,0,0}, new double[]{0}));
        ds.addRow(new DataSetRow(new double[]{1,0,0,0,1}, new double[]{0}));
        
        // Se procede a entrenar la red neuronal
        nn.learn(ds);
        nn.save("C:\\Users\\LLira\\Desktop\\nn\\Credit.nnet");
        
        int amount, age, family, job, saving;
        double y;
        DecimalFormat df = new DecimalFormat("##.##");
        
        amount = cmbAmount.getSelectedIndex()-2;
        age = cmbAge.getSelectedIndex()-2;
        if (rbtnFamilyMarried.isSelected()) {
            family = 1;
        }else{
            family = 0;
        }
        job = cmbJob.getSelectedIndex()-2;
        saving = cmbSaving.getSelectedIndex()-2;
        
        // Se procede a calcular la salida
        nn.setInput(amount, age, family, job, saving);
        nn.calculate();
        y = nn.getOutput()[0]; // Recuperamos el primer elemento
        
        txtY.setText("" + df.format(y));
    }//GEN-LAST:event_btnCalculateActionPerformed

