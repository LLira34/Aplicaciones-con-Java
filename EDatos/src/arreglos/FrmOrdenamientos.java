    private void btnOrdenarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOrdenarActionPerformed
        // TODO add your handling code here:

        int[][] matriz = null;
        int op; //Metodo de ordenamiento
        for (int i = 0; i < 10; i++) {
            datos[i] = Integer.parseInt(tblOriginal.getValueAt(0, i).toString());
        }
        op = cmbMetodo.getSelectedIndex();
        switch (op) {
            case 0:
                matriz = Ordenamientos.insercionDirecta(datos);
                break;
            case 1:
                matriz= Ordenamientos.burbuja(datos);
                break;
            case 2:
                break;
            case 3:
                Ordenamientos.quickSort(datos, 0, datos.length);
                break;
        }

        //Llenado de la tabla Vectores Ordenado
        for (int i = 0; i < 10; i++) {
            tblOrdenado.setValueAt(datos[i], 0, i);
        }
        //Llenado de la tabla Pasadas
        if (op != 3) {
            for (int i = 0; i < 9; i++) {
                for (int j = 0; j < 10; j++) {
                    tblPasadas.setValueAt(matriz[i][j], i, j + 1);
                }
            }
        }
    }//GEN-LAST:event_btnOrdenarActionPerformed

