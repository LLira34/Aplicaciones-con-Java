    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        if (txtTarea.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ingrese una tarea");
            return;
        }
        try {
            spnPrioridad.commitEdit();
        } catch (java.text.ParseException e) {
            e.printStackTrace();
        }
        int value = (Integer) spnPrioridad.getValue();
        cp.insertar(txtTarea.getText(), value);
        actualizarTables();
        getAtenderTarea();
        actualizarInfo();
        txtTarea.setText("");
    }//GEN-LAST:event_btnRegistrarActionPerformed

    private void btnAtenderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtenderActionPerformed
        String res = "";
        if (cp.getCola1().isEmpty() && cp.getCola2().isEmpty() && cp.getCola3().isEmpty()) {
            JOptionPane.showMessageDialog(this, "No hay tareas registadas");
        } else {
            res = cp.eliminar();
            lblUltTar.setText(res);
            actualizarTables();
            getAtenderTarea();
            actualizarInfo();
        }
    }//GEN-LAST:event_btnAtenderActionPerformed

