    public final void actualizarTables() {
        int a = 1;
        for (int i = 0; i < 6; i++) {
            tblAlta.setValueAt(cp.getCola1().getTarea(i), 0, a++);
        }
        a = 1;
        for (int i = 0; i < 6; i++) {
            tblMedia.setValueAt(cp.getCola2().getTarea(i), 0, a++);
        }
        a = 1;
        for (int i = 0; i < 6; i++) {
            tblBaja.setValueAt(cp.getCola3().getTarea(i), 0, a++);
        }
        a = 1;
    }

    public final void getAtenderTarea() {
        if (!cp.getCola1().isEmpty()) {
            lblTarAct.setText(cp.getCola1().getTarea(cp.getCola1().getFrente()));
        } else if (!cp.getCola1().isEmpty()) {
            lblTarAct.setText(cp.getCola2().getTarea(cp.getCola2().getFrente()));
        } else {
            lblTarAct.setText(cp.getCola3().getTarea(cp.getCola3().getFrente()));
        }
        if (cp.getCola1().isEmpty() && cp.getCola2().isEmpty() && cp.getCola3().isEmpty()) {
            lblTarAct.setText("...");
        }
    }

    public final void actualizarInfo() {
        lblPenAlt.setText("" + cp.getCola1().getTot());
        lblPenMed.setText("" + cp.getCola2().getTot());
        lblPenTri.setText("" + cp.getCola3().getTot());
        lblPenTot.setText("" + (cp.getCola1().getTot() + cp.getCola2().getTot() + cp.getCola3().getTot()));
    }

